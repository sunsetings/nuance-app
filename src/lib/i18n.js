function fill(template, vars = {}) {
  return String(template).replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? `{${key}}`));
}

function getValue(locale, key) {
  return key.split(".").reduce((acc, part) => (acc && acc[part] !== undefined ? acc[part] : undefined), dictionaries[locale]);
}

export function resolveLocale(input) {
  const raw = String(input || "").toLowerCase();
  if (raw.startsWith("ko")) return "ko";
  if (raw.startsWith("ja")) return "ja";
  if (raw.startsWith("es")) return "es";
  return "en";
}

export function getPreferredLocale() {
  if (typeof navigator === "undefined") return "en";
  const candidates = [...(navigator.languages || []), navigator.language].filter(Boolean);
  return resolveLocale(candidates[0]);
}

export function translate(locale, key, vars) {
  const resolved = resolveLocale(locale);
  const value = getValue(resolved, key) ?? getValue("en", key) ?? key;
  if (typeof value !== "string") return value;
  return fill(value, vars);
}

export function createI18n(locale = getPreferredLocale()) {
  const resolved = resolveLocale(locale);
  return {
    locale: resolved,
    t: (key, vars) => translate(resolved, key, vars),
  };
}
